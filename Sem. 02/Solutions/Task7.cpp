#include <iostream>
using namespace std;

/*
Задача 1: Да се напише програма, която приема поток от числа. 
Числата се събират, докато не срещне числото 0.
          Тогава трябва да се отпечата акумулираният 
		  резултат и да програмта да приключи.

Вход: 1 2 3 4 5 6 0
Изход: 21
*/

int main() {
	int num, sum;
	while (num != 0) {
		cin >> num;
		sum += num;
	}
}